const fs = require('fs');
const path = require('path');

const MAGAZINES_DIR = path.join(__dirname, 'Magazines');
const OUTPUT = path.join(__dirname, 'magazine-manifest.js');

function scan() {
  const manifest = [];

  let entries;
  try {
    entries = fs.readdirSync(MAGAZINES_DIR, { withFileTypes: true });
  } catch {
    console.error('Magazines/ directory not found.');
    process.exit(1);
  }

  const dirs = entries
    .filter((e) => e.isDirectory())
    .sort((a, b) => b.name.localeCompare(a.name));

  for (const dir of dirs) {
    const dirPath = path.join(MAGAZINES_DIR, dir.name);
    const files = fs.readdirSync(dirPath);

    const hasIndex = files.includes('index.html');
    if (!hasIndex) continue;

    const cover = files.find((f) => /^cover\.(jpg|jpeg|png|gif|webp)$/i.test(f));
    const title = deriveTitle(dir.name, dirPath, files);

    manifest.push({
      year: dir.name,
      title,
      cover: cover ? `Magazines/${dir.name}/${cover}` : null,
      href: `Magazines/${dir.name}/`,
    });
  }

  const code = `/**
 * magazine-manifest.js
 * Auto-generated by generate-magazine-manifest.js
 * Run \`node generate-magazine-manifest.js\` after adding magazines.
 */

const MAGAZINE_MANIFEST = ${JSON.stringify(manifest, null, 2)};
`;

  fs.writeFileSync(OUTPUT, code, 'utf-8');
  console.log(`✓ Wrote ${OUTPUT}`);
  console.log(`  Magazines found: ${manifest.length}`);
  for (const m of manifest) {
    console.log(`  - ${m.year}: ${m.title}`);
  }
}

function deriveTitle(year, dirPath, files) {
  const metaPath = path.join(dirPath, 'meta.json');
  if (fs.existsSync(metaPath)) {
    try {
      const meta = JSON.parse(fs.readFileSync(metaPath, 'utf-8'));
      if (meta.title) return meta.title;
    } catch {}
  }

  const htmlPath = path.join(dirPath, 'index.html');
  if (fs.existsSync(htmlPath)) {
    const html = fs.readFileSync(htmlPath, 'utf-8');
    const match = html.match(/<title>(.*?)<\/title>/i);
    if (match) return match[1];
  }

  return `Magazine ${year}`;
}

scan();
