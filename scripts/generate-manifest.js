const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const GALLERIES_DIR = path.join(ROOT, 'Galleries');
const OUTPUT = path.join(ROOT, 'data', 'gallery-manifest.js');
const SUPPORTED = /\.(jpg|jpeg|png|gif|webp|avif)$/i;

function scan() {
  const manifest = {};
  const allPhotos = [];

  let entries;
  try {
    entries = fs.readdirSync(GALLERIES_DIR, { withFileTypes: true });
  } catch {
    console.error('Galleries/ directory not found.');
    process.exit(1);
  }

  const yearDirs = entries
    .filter((e) => e.isDirectory() && /^\d{4}$/.test(e.name))
    .sort((a, b) => b.name.localeCompare(a.name));

  for (const dir of yearDirs) {
    const yearPath = path.join(GALLERIES_DIR, dir.name);
    const files = fs
      .readdirSync(yearPath)
      .filter((f) => SUPPORTED.test(f))
      .sort();

    const relPaths = files.map((f) => `Galleries/${dir.name}/${f}`);
    manifest[dir.name] = relPaths;
    allPhotos.push(...relPaths);
  }

  const preview = allPhotos.slice(0, 6);

  const code = `/**
 * gallery-manifest.js
 * Auto-generated by generate-manifest.js — do not edit manually.
 * Run \`node generate-manifest.js\` after adding images to Galleries/<YEAR>/
 */

const GALLERY_MANIFEST = ${JSON.stringify(manifest, null, 2)};

const GALLERY_PREVIEW = ${JSON.stringify(preview, null, 2)};
`;

  fs.writeFileSync(OUTPUT, code, 'utf-8');
  console.log(`✓ Wrote ${OUTPUT}`);
  console.log(`  Years: ${Object.keys(manifest).join(', ') || '(none)'}`);
  const total = allPhotos.length;
  console.log(`  Total photos: ${total}`);
}

scan();
