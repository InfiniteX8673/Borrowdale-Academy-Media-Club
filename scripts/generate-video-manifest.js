const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const VIDEOS_DIR = path.join(ROOT, 'Videos');
const OUTPUT = path.join(ROOT, 'data', 'video-manifest.js');
const SUPPORTED = /\.(mp4|webm|mov|avi|ogv)$/i;
const THUMB_EXTS = /\.(jpg|jpeg|png|webp|avif)$/i;

function scan() {
  const manifest = [];

  let entries;
  try {
    entries = fs.readdirSync(VIDEOS_DIR, { withFileTypes: true });
  } catch {
    console.error('Videos/ directory not found.');
    process.exit(1);
  }

  const yearDirs = entries
    .filter((e) => e.isDirectory() && /^\d{4}$/.test(e.name))
    .sort((a, b) => b.name.localeCompare(a.name));

  for (const dir of yearDirs) {
    const yearPath = path.join(VIDEOS_DIR, dir.name);
    const files = fs.readdirSync(yearPath);

    // 1. Read videos.json if present (for YouTube embeds and custom metadata)
    const jsonPath = path.join(yearPath, 'videos.json');
    if (fs.existsSync(jsonPath)) {
      try {
        const entries = JSON.parse(fs.readFileSync(jsonPath, 'utf-8'));
        if (Array.isArray(entries)) {
          for (const entry of entries) {
            manifest.push({
              title: entry.title || deriveTitle(entry.src || ''),
              src: entry.src || '',
              thumbnail: entry.thumbnail || null,
              year: dir.name,
            });
          }
        }
      } catch (err) {
        console.warn(`  Warning: could not parse ${jsonPath}: ${err.message}`);
      }
    }

    // 2. Scan for local video files
    const videoFiles = files
      .filter((f) => SUPPORTED.test(f) && !f.startsWith('.'))
      .sort();

    for (const f of videoFiles) {
      // Skip if already added via videos.json
      const alreadyAdded = manifest.some(
        (v) => v.year === dir.name && v.src.includes(f)
      );
      if (alreadyAdded) continue;

      manifest.push({
        title: deriveTitle(f),
        src: `Videos/${dir.name}/${f}`,
        thumbnail: findThumbnail(files, f, dir.name),
        year: dir.name,
      });
    }
  }

  const code = `/**
 * video-manifest.js
 * Auto-generated by generate-video-manifest.js — do not edit manually.
 * Run \`node generate-video-manifest.js\` after adding videos to Videos/<YEAR>/
 *
 * To add YouTube videos, create Videos/<YEAR>/videos.json:
 * [
 *   { "title": "My Video", "src": "https://www.youtube.com/embed/VIDEO_ID", "thumbnail": null }
 * ]
 *
 * To add local videos, drop .mp4/.webm/.mov files into Videos/<YEAR>/
 */

const VIDEO_MANIFEST = ${JSON.stringify(manifest, null, 2)};
`;

  fs.writeFileSync(OUTPUT, code, 'utf-8');
  console.log(`✓ Wrote ${OUTPUT}`);
  console.log(`  Years: ${yearDirs.map((d) => d.name).join(', ') || '(none)'}`);
  console.log(`  Total videos: ${manifest.length}`);
  for (const v of manifest) {
    console.log(`  - [${v.year}] ${v.title}`);
  }
}

function deriveTitle(name) {
  // Remove extension and clean up filename
  return name
    .replace(/\.(mp4|webm|mov|avi|ogv)$/i, '')
    .replace(/[-_]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim() || 'Untitled Video';
}

function findThumbnail(files, videoFile, year) {
  const base = videoFile.replace(SUPPORTED, '');
  const match = files.find((f) => {
    if (!THUMB_EXTS.test(f)) return false;
    return f.replace(THUMB_EXTS, '') === base;
  });
  return match ? `Videos/${year}/${match}` : null;
}

scan();
